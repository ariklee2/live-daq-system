def celsius_to_fahrenheit(celsius):
    return celsius * 9 / 5 + 32

def interpolate(voltage, voltage1, temp1, voltage2, temp2):
    return temp1 + (temp2 - temp1) / (voltage2 - voltage1) * (voltage - voltage1)

def voltage_to_psi(voltage):
    return ((voltage - 0.5) / (4.5 - 0.5)) * 1600

